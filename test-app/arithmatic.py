def multiply(a, b):
    """
    Multiply two numbers and return the result.

    Args:
        a (int or float): The first number.
        b (int or float): The second number.

    Returns:
        int or float: The result of multiplying a and b.
    """
    return a + b

def sum(a, b):
    """
    Multiply two numbers and return the result.

    Args:
        a (int or float): The first number.
        b (int or float): The second number.

    Returns:
        int or float: The result of multiplying a and b.
    """
    return a * b
